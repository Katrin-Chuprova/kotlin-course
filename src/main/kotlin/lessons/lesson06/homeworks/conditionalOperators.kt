/*
Задача 1 "Определение сезона"
Напишите функцию, которая на основе номера месяца распечатывает сезон года.
Номера месяцев начинаются с единицы.
*/
fun season(month: Int) {
    if (month == 12 || month == 1 || month == 2) {
        println("Зима")
    } else if (month in 3..5) {
        println("Весна")
    } else if (month in 6..8) {
        println("Лето")
    } else if (month in 9..11) {
        println("Осень")
    } else {
        println("Некорректный номер месяца")
    }
}

/*
Задание 2: "Расчет возраста питомца"
Создайте функцию, которая преобразует возраст собаки в "человеческие" годы.
До 2 лет каждый год собаки равен 10.5 человеческим годам,
после - каждый год равен 4 человеческим годам.
Результат распечатай в консоль.
 */
fun dogToHumanAge(dogAge: Int) {
    val humanAge = if (dogAge <= 2) {
        dogAge * 10.5
    } else {
        (2*10.5) + (dogAge - 2) * 4
    }
    println("Возраст собаки $dogAge лет равен $humanAge человеческим годам")
}

/*
Задание 3: "Определение способа перемещения"
Напишите функцию, которая печатает в консоль, какой способ перемещения лучше использовать,
исходя из длины маршрута. Если маршрут до 1 км - "пешком", до 5 км - "велосипед",
иначе - "автотранспорт".
 */
fun methodOfMovement(distance: Int) {
    if (distance <= 1) {
        println("Пешком")
    } else if (distance in 2..5) {
        println("Велосипед")
    } else {
        println("Автотранспорт")
    }
}

/*
Задание 4: "Расчет бонусных баллов"
Клиенты интернет-магазина получают бонусные баллы за покупки.
Напишите функцию, которая принимает сумму покупки и печатает в консоль количество бонусных баллов:
2 балла за каждые 100 рублей при сумме покупки до 1000 рублей и 3 балла за каждые 100 рублей при сумме свыше этого.
 */
fun bonusPoints(amount: Int) {
    val bonus = when {
        amount <= 1000 -> (amount / 100) * 2
        else -> (amount / 100) * 3
    }
    println("Сумма покупки: $amount рублей, бонусные баллы: $bonus")
}

/*
Задание 5: "Определение типа документа"
В системе хранения документов каждый файл имеет расширение.
Напишите функцию, которая на основе расширения файла печатает в консоль его тип:
"Текстовый документ", "Изображение", "Таблица" или "Неизвестный тип".
 */
fun docCategory(doc: String) {
    when (doc) {
        "txt", "docx", "odt", "rtf" -> println("Текстовый документ")
        "jpg", "png" -> println("Изображение")
        "xls", "xlsx", "scv" -> println("Таблица")
        else -> println("Неизвестный тип")
    }
}

/*
Задание 6: "Конвертация температуры"
Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты
и наоборот в зависимости от указанной единицы измерения (C/F).
Единицу измерения нужно передать вторым аргументом функции.
Несколько аргументов передаются через запятую.
Распечатай в консоль результат конвертации с добавлением единицы измерения.
Чтобы добавить единицу измерения после результата используй функцию печати без переноса
строки print("C") или print("F").
 */
fun convertTemperature(value: Double, unit: String) {
    when (unit.uppercase()) {
        "C" -> {
            val result = value * 9 / 5 + 32
            print(result)
            println("F")
        }
        "F" -> {
            val result = (value - 32) * 5 / 9
            print(result)
            println("C")
        }
        else -> println("Неизвестная единица измерения: $unit")
    }
}

/*
Задание 7: "Подбор одежды по погоде"
Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды:
"куртка и шапка" при температуре ниже +10, "ветровка" от +10 до +18 градусов включительно
и "футболка и шорты" при температуре выше +18 градусов. При температурах ниже -30 и выше +35
рекомендуйте не выходить из дома.
 */
fun clothingRecommendation(temperature: Int) {
    if (temperature < -30 || temperature > 35) {
        println("Лучше не выходить из дома")
    } else if (temperature < 10) {
        println("Куртка и шапка")
    } else if (temperature in 10..18) {
        println("Ветровка")
    } else {
        println("Футболка и шорты")
    }
}

